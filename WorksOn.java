
public class WorksOn {
	private int eNumber;
	private int pNumber;
	private int hours;

	public WorksOn() {
	}

	public WorksOn(int eNumber, int pNumber, int hours) {
		this.eNumber = eNumber;
		this.pNumber = pNumber;
		this.hours = hours;
	}

	public int geteNumber() {
		return eNumber;
	}
	
	public int getpNumber() {
		return pNumber;
	}
	
	public int getHours() {
		return hours;
	}
}
